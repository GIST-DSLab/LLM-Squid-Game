@pytest.fixture
def stub_provider_factory() -> Callable[..., StubProvider]:
    """Return a factory that creates fresh ``StubProvider`` instances.

    Tests inject a custom ``response_fn`` when they need to alternate
    actions per turn (e.g. forfeit scenario).
    """

    def _factory(**kwargs: Any) -> StubProvider:
        return StubProvider(**kwargs)

    return _factory


@pytest.fixture
def patch_runner_provider(monkeypatch: pytest.MonkeyPatch):
    """Patch ``ExperimentRunner._create_provider`` to yield a stub.

    Usage::

        stub = patch_runner_provider()           # default response
        stub = patch_runner_provider(response_fn=my_fn)

    Returns a function that, when called, installs the patch and returns
    the ``StubProvider`` that will be handed out by the factory. The
    same instance is reused for every season in the experiment so tests
    can inspect aggregated calls.
    """
    from squid_game.runner import ExperimentRunner

    def _install(**stub_kwargs: Any) -> StubProvider:
        stub = StubProvider(**stub_kwargs)

        def _fake_create_provider(_provider_config):  # type: ignore[no-untyped-def]
            return stub

        monkeypatch.setattr(
            ExperimentRunner, "_create_provider", staticmethod(_fake_create_provider)
        )
        return stub

    return _install
